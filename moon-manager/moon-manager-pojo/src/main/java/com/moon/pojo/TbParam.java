package com.moon.pojo;

public class TbParam {
    private Long id;

    private String number;

    private Float pressure;

    private Float electric;

    private Float activePower;

    private Float reactivePower;

    private Float windSpeed;

    private Float windAngle;

    private Float rotationSpeed;

    private Float temperature;

    private Float cableAngle;

    private Float dynamoTem;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number == null ? null : number.trim();
    }

    public Float getPressure() {
        return pressure;
    }

    public void setPressure(Float pressure) {
        this.pressure = pressure;
    }

    public Float getElectric() {
        return electric;
    }

    public void setElectric(Float electric) {
        this.electric = electric;
    }

    public Float getActivePower() {
        return activePower;
    }

    public void setActivePower(Float activePower) {
        this.activePower = activePower;
    }

    public Float getReactivePower() {
        return reactivePower;
    }

    public void setReactivePower(Float reactivePower) {
        this.reactivePower = reactivePower;
    }

    public Float getWindSpeed() {
        return windSpeed;
    }

    public void setWindSpeed(Float windSpeed) {
        this.windSpeed = windSpeed;
    }

    public Float getWindAngle() {
        return windAngle;
    }

    public void setWindAngle(Float windAngle) {
        this.windAngle = windAngle;
    }

    public Float getRotationSpeed() {
        return rotationSpeed;
    }

    public void setRotationSpeed(Float rotationSpeed) {
        this.rotationSpeed = rotationSpeed;
    }

    public Float getTemperature() {
        return temperature;
    }

    public void setTemperature(Float temperature) {
        this.temperature = temperature;
    }

    public Float getCableAngle() {
        return cableAngle;
    }

    public void setCableAngle(Float cableAngle) {
        this.cableAngle = cableAngle;
    }

    public Float getDynamoTem() {
        return dynamoTem;
    }

    public void setDynamoTem(Float dynamoTem) {
        this.dynamoTem = dynamoTem;
    }
}